/// Combined rational resampler with optional power-of-2 pre-decimation.
pub struct RationalResampler<T: Sample> {
    in_rate: f64,
    out_rate: f64,
    mode: Mode,
    predec_stages: Vec<DecimatingFir<T>>,
    resamp: Option<PolyphaseResampler<T>>,
    scratch: Vec<T>,
    temp: Vec<T>,
}

#[derive(Clone, Copy)]
enum Mode {
    None,
    DecimOnly,
    ResampOnly,
    Both,
}

impl<T: Sample> RationalResampler<T> {
    /// Create a new resampler and configure it for the given rates.
    pub fn new(in_rate: f64, out_rate: f64) -> Self {
        let mut me = Self {
            in_rate,
            out_rate,
            mode: Mode::None,
            predec_stages: Vec::new(),
            resamp: None,
            scratch: Vec::new(),
            temp: Vec::new(),
        };
        me.reconfigure();
        me
    }

    /// Update sample rates and reconfigure internal filters.
    pub fn set_rates(&mut self, in_rate: f64, out_rate: f64) {
        self.in_rate = in_rate;
        self.out_rate = out_rate;
        self.reconfigure();
    }

    /// Reset internal state (delay lines, phase accumulators).
    pub fn reset(&mut self) {
        for stage in &mut self.predec_stages {
            stage.reset();
        }
        if let Some(r) = &mut self.resamp {
            r.reset();
        }
    }

    /// Process a block of input samples and write to `output`.
    /// Returns the number of output samples produced.
    pub fn process(&mut self, input: &[T], output: &mut [T]) -> usize {
        // Ensure internal buffers can hold the worst-case intermediate size.
        let max_len = input.len().max(output.len());
        if self.scratch.len() < max_len {
            self.scratch.resize(max_len, T::default());
        }
        if self.temp.len() < max_len {
            self.temp.resize(max_len, T::default());
        }

        match self.mode {
            Mode::None => {
                let n = input.len().min(output.len());
                output[..n].copy_from_slice(&input[..n]);
                n
            }
            Mode::DecimOnly => Self::run_predec(&mut self.predec_stages, &mut self.temp, input, output),
            Mode::ResampOnly => {
                self.resamp.as_mut().unwrap().process(input, output)
            }
            Mode::Both => {
                let n = Self::run_predec(&mut self.predec_stages, &mut self.temp, input, &mut self.scratch);
                self.resamp.as_mut().unwrap().process(&self.scratch[..n], output)
            }
        }
    }

    /// Run pre-decimation stages, writing into `dst`.
    fn run_predec(stages: &mut [DecimatingFir<T>], temp: &mut Vec<T>, input: &[T], dst: &mut [T]) -> usize {
        if stages.is_empty() {
            let n = input.len().min(dst.len());
            dst[..n].copy_from_slice(&input[..n]);
            return n;
        }

        let mut n = stages[0].process(input, dst);
        for stage in &mut stages[1..] {
            n = stage.process(&dst[..n], temp);
            dst[..n].copy_from_slice(&temp[..n]);
        }
        n
    }

    fn reconfigure(&mut self) {
        const MAX_RATIO_POWER: usize = 13; // 8192

        let ratio = self.in_rate / self.out_rate;
        let predec_power = if ratio > 1.0 {
            (ratio.log2().floor() as usize).min(MAX_RATIO_POWER)
        } else {
            0
        };
        let predec_ratio = 1usize << predec_power;

        let mut int_sample_rate = self.in_rate;
        let use_decim = self.in_rate > self.out_rate && predec_power > 0;

        self.predec_stages.clear();

        if use_decim && predec_ratio > 1 {
            int_sample_rate = self.in_rate / predec_ratio as f64;
            let plan_id = predec_power - 1;
            if let Some(plan) = DECIM_PLANS.get(plan_id) {
                for stage in plan.stages {
                    let taps = stage.taps.to_vec();
                    self.predec_stages.push(DecimatingFir::new(taps, stage.decimation));
                }
            }
        }

        let int_sr = int_sample_rate.round() as usize;
        let out_sr = self.out_rate.round() as usize;
        let g = gcd(int_sr, out_sr);
        let interp = if g == 0 { 1 } else { out_sr / g };
        let decim = if g == 0 { 1 } else { int_sr / g };

        let actual_out_sr = int_sample_rate * interp as f64 / decim as f64;
        let error = ((actual_out_sr - self.out_rate) / self.out_rate).abs() * 100.0;
        if error > 0.01 {
            eprintln!("Warning: resampling error is over 0.01%: {}", error);
        }

        if interp == decim {
            self.mode = if use_decim { Mode::DecimOnly } else { Mode::None };
            self.resamp = None;
            return;
        }

        let tap_sr = int_sample_rate * interp as f64;
        let tap_bw = self.in_rate.min(self.out_rate) / 2.0;
        let tap_tw = tap_bw * 0.1;
        let mut taps = generate_low_pass_taps(tap_bw, tap_tw, tap_sr);
        for t in &mut taps {
            *t *= interp as f32;
        }

        self.resamp = Some(PolyphaseResampler::new(interp, decim, &taps));
        self.mode = if use_decim { Mode::Both } else { Mode::ResampOnly };

        eprintln!(
            "[Resamp] predec: {}, interp: {}, decim: {}, inacc: {}%, taps: {}",
            predec_ratio, interp, decim, error, taps.len()
        );
    }
}

