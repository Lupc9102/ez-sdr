#[inline]
fn sinc(x: f64) -> f64 {
    if x.abs() < 1e-12 {
        1.0
    } else {
        x.sin() / x
    }
}

/// Nuttall window (standard 4-term formulation).
#[inline]
fn nuttall_window(i: usize, n: usize) -> f64 {
    let a0 = 0.355768;
    let a1 = 0.487396;
    let a2 = 0.144232;
    let a3 = 0.012604;
    let n1 = n.saturating_sub(1) as f64;
    let x = i as f64;
    if n1 <= 0.0 {
        return 1.0;
    }
    a0 - a1 * (2.0 * std::f64::consts::PI * x / n1).cos()
        + a2 * (4.0 * std::f64::consts::PI * x / n1).cos()
        - a3 * (6.0 * std::f64::consts::PI * x / n1).cos()
}

/// Generate low-pass FIR taps using windowed sinc.
fn generate_low_pass_taps(cutoff: f64, trans_width: f64, sample_rate: f64) -> Vec<f32> {
    let count = (3.8 * sample_rate / trans_width).ceil().max(1.0) as usize;
    let omega = 2.0 * std::f64::consts::PI * cutoff / sample_rate;
    let half = (count as f64 - 1.0) / 2.0;
    let corr = omega / std::f64::consts::PI;

    let mut taps = Vec::with_capacity(count);
    for i in 0..count {
        let t = i as f64 - half;
        let s = if t.abs() < 1e-12 {
            1.0
        } else {
            sinc(t * omega)
        };
        let w = nuttall_window(i, count);
        taps.push((s * w * corr) as f32);
    }
    taps
}

#[inline]
fn gcd(mut a: usize, mut b: usize) -> usize {
    while b != 0 {
        let t = b;
        b = a % b;
        a = t;
    }
    a
}

