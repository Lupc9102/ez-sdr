/// Single-stage decimating FIR.
struct DecimatingFir<T: Sample> {
    taps: Vec<f32>,
    decimation: usize,
    delay: Vec<T>,
    offset: usize,
}

impl<T: Sample> DecimatingFir<T> {
    fn new(taps: Vec<f32>, decimation: usize) -> Self {
        let delay_len = taps.len().saturating_sub(1);
        Self {
            taps,
            decimation,
            delay: vec![T::default(); delay_len],
            offset: 0,
        }
    }

    fn reset(&mut self) {
        self.delay.fill(T::default());
        self.offset = 0;
    }

    /// Process `input` and write decimated samples into `output`.
    /// Returns the number of output samples written.
    fn process(&mut self, input: &[T], output: &mut [T]) -> usize {
        let tap_count = self.taps.len();
        let total = self.delay.len() + input.len();
        let mut out_count = 0;
        let mut offset = self.offset;

        while offset < total {
            if out_count >= output.len() {
                break;
            }

            let mut acc = T::default();
            for i in 0..tap_count {
                let idx = offset + i;
                let sample = if idx < self.delay.len() {
                    self.delay[idx]
                } else {
                    input[idx - self.delay.len()]
                };
                acc = acc + sample * self.taps[i];
            }
            output[out_count] = acc;
            out_count += 1;
            offset += self.decimation;
        }

        self.offset = offset.saturating_sub(total);

        // Update delay line: keep last `tap_count - 1` samples.
        let keep = tap_count.saturating_sub(1);
        if keep > 0 {
            let start = total.saturating_sub(keep);
            let mut new_delay = Vec::with_capacity(keep);
            for i in start..total {
                let sample = if i < self.delay.len() {
                    self.delay[i]
                } else {
                    input[i - self.delay.len()]
                };
                new_delay.push(sample);
            }
            self.delay = new_delay;
        }

        out_count
    }
}

/// Polyphase rational resampler: interpolation + FIR + decimation in one step.
struct PolyphaseResampler<T: Sample> {
    interp: usize,
    decim: usize,
    phases: Vec<Vec<f32>>,
    delay: Vec<T>,
    phase: usize,
    offset: usize,
}

impl<T: Sample> PolyphaseResampler<T> {
    fn new(interp: usize, decim: usize, taps: &[f32]) -> Self {
        let phases = build_polyphase_bank(interp, taps);
        let tpp = phases[0].len();
        Self {
            interp,
            decim,
            phases,
            delay: vec![T::default(); tpp.saturating_sub(1)],
            phase: 0,
            offset: 0,
        }
    }

    fn reset(&mut self) {
        self.delay.fill(T::default());
        self.phase = 0;
        self.offset = 0;
    }

    fn process(&mut self, input: &[T], output: &mut [T]) -> usize {
        let tpp = self.phases[0].len();
        let total = self.delay.len() + input.len();
        let mut out_count = 0;
        let mut phase = self.phase;
        let mut offset = self.offset;

        while offset < total {
            if out_count >= output.len() {
                break;
            }

            let taps = &self.phases[phase];
            let mut acc = T::default();
            for i in 0..tpp {
                let idx = offset + i;
                let sample = if idx < self.delay.len() {
                    self.delay[idx]
                } else {
                    input[idx - self.delay.len()]
                };
                acc = acc + sample * taps[i];
            }
            output[out_count] = acc;
            out_count += 1;

            phase += self.decim;
            offset += phase / self.interp;
            phase %= self.interp;
        }

        self.phase = phase;
        self.offset = offset.saturating_sub(total);

        // Update delay line.
        let keep = tpp.saturating_sub(1);
        if keep > 0 {
            let start = total.saturating_sub(keep);
            let mut new_delay = Vec::with_capacity(keep);
            for i in start..total {
                let sample = if i < self.delay.len() {
                    self.delay[i]
                } else {
                    input[i - self.delay.len()]
                };
                new_delay.push(sample);
            }
            self.delay = new_delay;
        }

        out_count
    }
}

/// Build polyphase filter bank. Mirrors C++ `buildPolyphaseBank` exactly.
fn build_polyphase_bank(interp: usize, taps: &[f32]) -> Vec<Vec<f32>> {
    let tpp = (taps.len() + interp - 1) / interp;
    let mut phases = vec![vec![0.0f32; tpp]; interp];
    let total = interp * tpp;
    for i in 0..total {
        let val = if i < taps.len() { taps[i] } else { 0.0 };
        phases[(interp - 1) - (i % interp)][i / interp] = val;
    }
    phases
}

